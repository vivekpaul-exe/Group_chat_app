class MessageForm extends React.Component {
  constructor(){
    super()
    this.state = {
      message : null,
    }
    this.handleChange = this.handleChange.bind(this);
    this.handleSubmit = this.handleSubmit.bind(this);
  }
  handleChange(e){
    this.setState({
        message : e.target.value
    }
    )
  }
  handleSubmit(e){
    e.preventDefalt();
    if(this.state.text.length === 0){
      return;
    }
    
  }
  render() {
    return (
      <React.Fragment>
        <Paper elevation={3} >
          <form className ="form-group" style={{display:"flex"}}>
            <input
              type ="text" 
              className="form-control"
              id="form-control"
              value={this.message}
              onChange = {this.handleChange}
              />
              <button type="button" className="btn btn-outline-primary" data-toggle="button" aria-pressed="false" onClick={this.handleClick}>
                <SendIcon />
              </button>
           
          </form>

        </Paper>
      </React.Fragment>
    )
  }
}